function Test-IsGuid {
    [OutputType([bool])]
    param
    (
        [Parameter(Mandatory = $true, Position = 0)]
        [string]$ObjectGuid
    )
	
    # Define verification regex
    [regex]$guidRegex = '(?im)^[{(]?[0-9A-F]{8}[-]?(?:[0-9A-F]{4}[-]?){3}[0-9A-F]{12}[)}]?$'

    # Check guid against regex
    return $ObjectGuid -match $guidRegex
}